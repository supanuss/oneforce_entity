def route_reflection(state: AgentState) -> str:
    """Conditional router function to determine if extraction should loop back or move on (read-only)."""
    feedback = state.get("reflection_feedback", "")
    idx = state["current_index"]
    cases = state["cases"]
    
    if feedback:
        # We set reflection_feedback, meaning the auditor requested a retry
        return "retry"
    else:
        # No feedback means we successfully committed this case and incremented current_index
        if idx < len(cases):
            return "continue"
        return "consolidate"

