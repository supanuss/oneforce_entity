        # 4. Process Scammer Names
        for name in case_data.get("scammer_names", []):
            if name not in unique_scammers:
                unique_scammers[name] = {
                    "name": name,
                    "linked_cases": [case_id],
                    "associated_banks": []
                }
            else:
                if case_id not in unique_scammers[name]["linked_cases"]:
                    unique_scammers[name]["linked_cases"].append(case_id)
                    
            # Link banks used in this case to this scammer name
            for bank in case_data.get("bank_accounts", []):
                acct_num = bank.get("account_number")
