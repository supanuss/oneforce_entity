def upload_to_neo4j(state: AgentState) -> AgentState:
    """Node to connect to Neo4j database and populate graph data using optimized batch UNWIND queries."""
    print("\n[Agent] Uploading graph data to Neo4j database (Batch UNWIND style)...")
    graph_data = state["graph_data"]
    
    uri = os.getenv("NEO4J_URI", "bolt://localhost:7687")
    username = os.getenv("NEO4J_USERNAME", "neo4j")
    password = os.getenv("NEO4J_PASSWORD", "password")
    
    type_to_label = {
        "case": "Case",
        "person": "Person",
        "bank": "FinancialAccount",
        "channel": "OnlineAccount",
        "evidence": "Evidence",
        "location": "Location"
    }
    
    try:
        from collections import defaultdict
        
        with GraphDatabase.driver(uri, auth=(username, password)) as driver:
            with driver.session() as session:
                # 1. Create indices if they don't exist to ensure fast merges/matches
                print("[Neo4j] Ensuring indexes exist for all node types...")
                for label in type_to_label.values():
                    index_query = f"CREATE INDEX {label.lower()}_id_idx IF NOT EXISTS FOR (n:{label}) ON (n.id)"
                    session.run(index_query)
                
                # 2. Clear existing nodes related to these cases to prevent duplicates
                print("[Neo4j] Cleaning up existing nodes linked to these cases...")
                case_ids = [n["properties"].get("case_id") for n in graph_data["nodes"] if n["type"] == "case"]
                if case_ids:
                    session.run("""
                    MATCH (c:Case)
                    WHERE c.case_id IN $case_ids
                    DETACH DELETE c
                    """, case_ids=case_ids)
                    
                    # Clean up orphaned Person, Accounts, Locations, Evidence (no links to Case left)
                    session.run("""
                    MATCH (n)
                    WHERE (n:Person OR n:FinancialAccount OR n:OnlineAccount OR n:Evidence OR n:Location)
                      AND NOT (n)-[]-(:Case)
                    DETACH DELETE n
                    """)
                
                # 3. Group and Batch Upload Nodes
                print("[Neo4j] Batch uploading nodes...")
                nodes_by_label = defaultdict(list)
                for node in graph_data["nodes"]:
                    label = type_to_label.get(node["type"], "Entity")
                    props = dict(node.get("properties", {}))
                    props["id"] = node["id"]
                    props["label"] = node["label"]
                    nodes_by_label[label].append(props)
                    
                for label, batch in nodes_by_label.items():
                    print(f"  - Uploading {len(batch)} nodes for label '{label}'...")
                    session.run(f"""
                    UNWIND $batch AS row
                    MERGE (n:{label} {{id: row.id}})
                    SET n += row
                    """, batch=batch)
                    
                # 4. Group and Batch Upload Edges
                print("[Neo4j] Batch uploading relationships...")
                nodes_dict = {node["id"]: node for node in graph_data["nodes"]}
                edges_by_group = defaultdict(list)
                
                for edge in graph_data["edges"]:
                    source_id = edge["source"]
                    target_id = edge["target"]
                    relation = edge["relation"]
                    props = edge.get("properties", {})
                    
                    src_label = type_to_label.get(nodes_dict[source_id]["type"], "Entity")
                    tgt_label = type_to_label.get(nodes_dict[target_id]["type"], "Entity")
                    
                    key = (src_label, tgt_label, relation)
                    edges_by_group[key].append({
                        "source_id": source_id,
                        "target_id": target_id,
                        "properties": props
                    })
                    
                for (src_label, tgt_label, relation), batch in edges_by_group.items():
                    print(f"  - Uploading {len(batch)} relationships of type: ({src_label})-[{relation}]->({tgt_label})...")
                    session.run(f"""
                    UNWIND $batch AS row
                    MATCH (a:{src_label} {{id: row.source_id}})
                    MATCH (b:{tgt_label} {{id: row.target_id}})
                    MERGE (a)-[r:{relation}]->(b)
                    SET r += row.properties
                    """, batch=batch)
                    
        print("✔ [Neo4j] Graph successfully uploaded to Neo4j in batches!")
        return {**state, "status": "Graph successfully uploaded to Neo4j"}
        
    except Exception as e:
        print(f"⚠ [Warning] Could not upload graph to Neo4j: {e}")
        print("Ensure your Neo4j docker container is running and connection details in .env are correct.")
        return {**state, "status": f"Graph built, but Neo4j upload failed/skipped: {e}"}
