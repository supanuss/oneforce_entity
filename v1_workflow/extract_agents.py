def validate_and_parse_json(raw_text: str, pydantic_model) -> dict:
    """
    Cleans raw text, extracts any JSON blocks (handles markdown blocks like ```json ... ```),
    and validates it against the given Pydantic model.
    Raises ValueError with validation error description if invalid.
    """
    if not raw_text:
        raise ValueError("Empty response received from LLM.")
        
    cleaned_text = raw_text.strip()
    
    # Remove <think>...</think> tags which are generated by reasoning models
    cleaned_text = re.sub(r"<think>.*?</think>", "", cleaned_text, flags=re.DOTALL).strip()

    # Remove markdown code block fences if present
    if cleaned_text.startswith("```"):
        match = re.search(r"```(?:json)?\s*(.*?)\s*```", cleaned_text, re.DOTALL)
        if match:
            cleaned_text = match.group(1).strip()
            
    # Simple JSON search fallback in case there is trailing text outside code blocks
    if not (cleaned_text.startswith("{") and cleaned_text.endswith("}")):
        match = re.search(r"(\{.*\})", cleaned_text, re.DOTALL)
        if match:
            cleaned_text = match.group(1).strip()
            
    try:
        data = json.loads(cleaned_text)
    except json.JSONDecodeError as e:
        raise ValueError(f"Invalid JSON Format: {str(e)}\nRaw Response: {raw_text}")
        
    # Validate using Pydantic
    try:
        validated_obj = pydantic_model(**data)
        return validated_obj.model_dump()
    except Exception as e:
        raise ValueError(f"JSON schema validation failed: {str(e)}")

def is_similar_to_accuser(name: str, accuser: str) -> bool:
    """
    Checks if a name is highly similar to the accuser's name using fuzzy matching (SequenceMatcher).
    Helps prevent slight spelling variations of the victim's name from leaking into scammer data.
    """
    if not name or not accuser:
        return False
        
    import difflib
    n = name.strip().lower()
    a = accuser.strip().lower()
    
    # Pre-clean: strip common titles
    titles = ["นางสาว", "นาง", "นาย", "ด.ญ.", "ด.ช.", "น.ส.", "mr.", "ms.", "mrs."]
    for title in titles:
        n = n.replace(title, "").strip()
        a = a.replace(title, "").strip()
        
    # Check if one is a substring of the other or vice versa
    if n in a or a in n:
        return True
        
    # Calculate similarity ratio
    ratio = difflib.SequenceMatcher(None, n, a).ratio()
    return ratio > 0.75  # 75% threshold is highly effective for Thai name spelling variations

def sanitize_extracted_data(extracted_data: dict, accuser: str) -> dict:
    """
    Programmatically sanitizes the extracted data to prevent victim info leakage (leakage guardrail)
    and strips placeholder mock data copied from prompts by smaller local LLMs.
    """
    if not extracted_data:
        return extracted_data
        
    accuser_clean = accuser.strip().lower()
    ignored_placeholders = ["สมชาย สมถาวร", "0123456789", "ชื่อเจ้าของบัญชีปลายทาง", "ชื่อมิจฉาชีพ1", "ชื่อมิจฉาชีพ2", "ชื่อธนาคาร", "เลขบัญชี", "ลิงก์หรือไอดี"]
    
    def is_placeholder(val: str) -> bool:
        if not val:
            return True
        val_clean = val.strip().lower()
        for p in ignored_placeholders:
            if p in val_clean:
                return True
        return False

    # 1. Clean scammer_names: remove victim's name or placeholder
    if "scammer_names" in extracted_data and isinstance(extracted_data["scammer_names"], list):
        filtered_names = []
        for name in extracted_data["scammer_names"]:
            if is_placeholder(name):
                print(f"[Guardrail] Automatically removed placeholder scammer name: '{name}'")
                continue
            if is_similar_to_accuser(name, accuser):
                print(f"[Guardrail] Automatically removed accuser name '{name}' (similar to '{accuser}') from scammer_names.")
                continue
            filtered_names.append(name)
        extracted_data["scammer_names"] = filtered_names
        
    # 2. Clean bank_accounts: ensure bank owner name is not the accuser or placeholder
    if "bank_accounts" in extracted_data and isinstance(extracted_data["bank_accounts"], list):
        filtered_accounts = []
        for acct in extracted_data["bank_accounts"]:
            owner = acct.get("owner_name", "")
            acct_num = acct.get("account_number", "")
            bank_name = acct.get("bank_name", "")
            
            if is_placeholder(owner) or is_placeholder(acct_num) or is_placeholder(bank_name):
                print(f"[Guardrail] Automatically removed placeholder bank account: {bank_name} - {acct_num} ({owner})")
                continue
            if owner and is_similar_to_accuser(owner, accuser):
                print(f"[Guardrail] Automatically removed bank account belonging to accuser: '{owner}' (similar to '{accuser}').")
                continue
            filtered_accounts.append(acct)
        extracted_data["bank_accounts"] = filtered_accounts
        
    # 3. Clean social channels: ensure social handles/IDs are not the accuser's name/ID or placeholders
    if "social_channels" in extracted_data and isinstance(extracted_data["social_channels"], list):
        filtered_socials = []
        for sc in extracted_data["social_channels"]:
            url_id = sc.get("url_or_id", "")
            name = sc.get("account_name", "")
            platform = sc.get("platform", "")
            
            if is_placeholder(url_id) or is_placeholder(name) or is_placeholder(platform):
                print(f"[Guardrail] Automatically removed placeholder social channel: {platform} ({name or url_id})")
                continue
            if (url_id and accuser_clean in url_id.lower()) or (name and is_similar_to_accuser(name, accuser)):
                print(f"[Guardrail] Automatically removed social channel linked to accuser '{name or url_id}'.")
                continue
            filtered_socials.append(sc)
        extracted_data["social_channels"] = filtered_socials
        
    return extracted_data

