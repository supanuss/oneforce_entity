def consolidate_blacklist(state: AgentState) -> AgentState:
    """Node to aggregate extracted entities into a structured blacklist database."""
    print("\n[Agent] Consolidating blacklist database...")
    extracted = state["extracted_entities"]
    
    # Unique banks, socials, phones, names
    unique_bank_accounts = {}
    unique_socials = {}
    unique_phones = set()
    unique_scammers = {}
    
    for case_data in extracted:
        case_id = case_data.get("case_id")
        
        # 1. Process Bank Accounts
        for bank in case_data.get("bank_accounts", []):
            acct_num = bank.get("account_number")
            owner = bank.get("owner_name")
            bank_name = bank.get("bank_name")
            if acct_num and owner:
                key = f"{bank_name}_{acct_num}"
                if key not in unique_bank_accounts:
                    unique_bank_accounts[key] = {
                        "bank_name": bank_name,
                        "account_number": acct_num,
                        "owner_name": owner,
                        "linked_cases": [case_id],
                        "associated_scammers": list(case_data.get("scammer_names", []))
                    }
                else:
                    if case_id not in unique_bank_accounts[key]["linked_cases"]:
                        unique_bank_accounts[key]["linked_cases"].append(case_id)
                    # Merge associated scammers
                    for s_name in case_data.get("scammer_names", []):
                        if s_name not in unique_bank_accounts[key]["associated_scammers"]:
                            unique_bank_accounts[key]["associated_scammers"].append(s_name)
                            
