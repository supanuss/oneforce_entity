    import shutil
    shutil.copy2(json_path, artifact_json_path)
    print(f"[Agent] Copied graph JSON to artifact folder: {artifact_json_path}")
    
    return {
        **state,
        "graph_data": graph_data,
        "status": "Network graph built successfully"
    }

