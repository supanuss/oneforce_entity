def build_network_graph(state: AgentState) -> AgentState:
    """Node to parse the extracted entities and build raw NetworkX nodes and edges matching Neo4j schema with properties."""
    print("\n[Agent] Building network graph structure (Neo4j-style with properties)...")
    extracted = state["extracted_entities"]
    
    nodes = []
    edges = []
    
    # Maintain node registries to prevent duplicate definitions
    seen_nodes = set()
    
    def add_node(node_id: str, label: str, node_type: str, properties: Dict[str, Any] = None):
        if node_id not in seen_nodes:
            seen_nodes.add(node_id)
            nodes.append({
                "id": node_id,
                "label": label,
                "type": node_type,
                "properties": properties or {}
            })
            
    def add_edge(source: str, target: str, rel_type: str, properties: Dict[str, Any] = None):
        edges.append({
            "source": source,
            "target": target,
            "relation": rel_type,
            "properties": properties or {}
        })
        
    for case_data in extracted:
        case_id = case_data.get("case_id")
        accuser = case_data.get("accuser")
        scam_type = case_data.get("scam_type", "Online Scam")
        damage = case_data.get("damage_amount", 0.0)
        
        # We can find the station info from the original cases list
        orig_case = next((c for c in state["cases"] if c.get("case_id") == case_id), {})
        station_name = orig_case.get("station", "")
        audio_url = orig_case.get("audio_url", "")
        
        # 1. Case Node (Rose Red)
        case_node_id = f"case_{case_id}"
        # Formatting loss amount for label display, e.g., 57K THB
        loss_label = f"{damage/1000:.1f}k THB" if damage >= 1000 else f"{damage:.0f} THB"
        case_label = f"Case: {case_id[:6]}...\n({loss_label})"
        
        add_node(case_node_id, case_label, "case", {
            "case_id": case_id,
            "scam_type": scam_type,
            "damage_amount": damage,
            "station": station_name
        })
        
        # 2. Victim Person Node (Green)
        if accuser:
            victim_node_id = f"person_{accuser}"
            add_node(victim_node_id, accuser, "person", {
                "name": accuser,
                "role": "victim"
            })
            add_edge(victim_node_id, case_node_id, "VICTIM_IN", {"role": "victim"})
            
        # 3. Suspect Person Node (Green) - "ผู้ต้องสงสัย"
        for name in case_data.get("scammer_names", []):
            suspect_node_id = f"person_{name}"
            add_node(suspect_node_id, name, "person", {
                "name": name,
                "role": "suspect"
            })
            add_edge(case_node_id, suspect_node_id, "SCAMMED_BY", {"role": "suspect"})
            
        # 4. Bank Account Node (Hot Pink) - "FinancialAccount"
        for bank in case_data.get("bank_accounts", []):
            acct_num = bank.get("account_number")
            bank_name = bank.get("bank_name")
            owner = bank.get("owner_name")
            if acct_num:
                bank_node_id = f"bank_{acct_num}"
                label = f"{bank_name}: {acct_num}"
                add_node(bank_node_id, label, "bank", {
                    "bank_name": bank_name,
                    "account_number": acct_num,
                    "owner": owner
                })
                # Link Case -> Bank Account
                add_edge(case_node_id, bank_node_id, "TRANSFERRED_TO", {
                    "bank_name": bank_name,
                    "account_number": acct_num,
                    "damage_amount": damage
                })
                
                # 5. Defendant / Account Owner Node (Green) - "จำเลย/เจ้าของบัญชี"
                if owner:
                    owner_node_id = f"person_{owner}"
                    add_node(owner_node_id, owner, "person", {
                        "name": owner,
                        "role": "defendant"
                    })
                    # Bank Account is owned by this person
                    add_edge(bank_node_id, owner_node_id, "OWNED_BY", {"role": "defendant"})
                    
        # 6. Social Channels (Orange) - "OnlineAccount"
        for social in case_data.get("social_channels", []):
            val = social.get("url_or_id")
            platform = social.get("platform")
            name = social.get("account_name")
            if val:
                channel_node_id = f"social_{platform}_{val}"
                label = f"{platform}: {name or val}"
                add_node(channel_node_id, label, "channel", {
                    "platform": platform,
                    "url_or_id": val,
                    "account_name": name
                })
                add_edge(case_node_id, channel_node_id, "CONTACTED_THROUGH", {
                    "platform": platform,
                    "url_or_id": val
                })
                
        # 7. Evidence Node (Sky Blue) - "Evidence"
        if audio_url:
            evidence_node_id = f"evidence_{case_id}"
            add_node(evidence_node_id, "Audio Recording", "evidence", {
                "audio_url": audio_url,
                "type": "audio"
            })
            add_edge(case_node_id, evidence_node_id, "HAS_EVIDENCE", {
                "type": "audio"
            })
            
        # 8. Location Node (Light Green) - "Location"
        if station_name:
            loc_node_id = f"location_{station_name}"
            add_node(loc_node_id, station_name, "location", {
                "station_name": station_name
            })
            add_edge(case_node_id, loc_node_id, "OCCURRED_AT", {
                "station_name": station_name
            })
            
    graph_data = {"nodes": nodes, "edges": edges}
    
    # Save the detailed graph data as a JSON file
    workspace_dir = "/Users/supanus/Desktop/oneforce/extrack_scammer"
    json_path = os.path.join(workspace_dir, "knowledge_graph.json")
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(graph_data, f, indent=2, ensure_ascii=False)
    print(f"[Agent] Saved detailed graph JSON to {json_path}")
    
    # Copy graph JSON to artifact folder
    artifact_dir = "/Users/supanus/.gemini/antigravity-ide/brain/2da58b1a-4845-4743-b79c-bd7d715bb846"
    os.makedirs(artifact_dir, exist_ok=True)
